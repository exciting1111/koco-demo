let selectedStars = 1;
let currentProductKey = null;

function openProduct(key) {
  const p = productData[key];
  if (!p) return;
  currentProductKey = key;
  const lang = currentLang;

  // Populate image
  if (p.img) {
    document.getElementById('modalImgWrap').innerHTML = `<img id="modalImg" src="${p.img}" alt="" />`;
  } else {
    document.getElementById('modalImgWrap').innerHTML = `<div style="font-size:96px;display:flex;align-items:center;justify-content:center;height:100%;background:#f5f5f5;border-radius:16px">${p.emoji || '📦'}</div>`;
  }

  document.getElementById('modalTitle').textContent = p.name;
  document.getElementById('modalRound').textContent = p.round;

  // Stars progress
  const totalStars = p.totalStars || 100;
  const currentStars = Math.round(totalStars * p.progress / 100);
  const remainStars = totalStars - currentStars;

  const dict = i18n[lang];
  document.getElementById('modalTotalStars').textContent = totalStars.toLocaleString();
  document.getElementById('modalCurrentStars').textContent = currentStars.toLocaleString();
  document.getElementById('modalProgress').textContent = p.progress + '%';
  document.getElementById('modalProgressFill').style.width = p.progress + '%';
  document.getElementById('modalProgressPreview').style.width = p.progress + '%';

  // Remain label
  const remainLabel = dict.modal_need_more
    ? dict.modal_need_more.replace('{n}', remainStars.toLocaleString())
    : `cần thêm ${remainStars.toLocaleString()} ⭐`;
  document.getElementById('modalRemainStars').textContent = remainLabel;

  // Reset star selection
  selectedStars = 1;
  document.querySelectorAll('.star-btn').forEach((b, i) => b.classList.toggle('active', i === 0));
  updateCost();

  // Show modal
  document.getElementById('productModal').classList.add('open');
  document.body.style.overflow = 'hidden';
  // Render winner reviews
  renderReviews(key);
}

function selectStars(btn, count) {
  selectedStars = count;
  document.querySelectorAll('.star-btn').forEach(b => b.classList.remove('active'));
  btn.classList.add('active');
  updateCost();
}

function updateCost() {
  const p = productData[currentProductKey];
  if (!p) return;

  // Cost: display as star count only (no currency)
  document.getElementById('modalCost').textContent = selectedStars + ' ⭐';

  // Calculate win probability
  const totalStars = p.totalStars || 100;
  const currentStars = Math.round(totalStars * p.progress / 100);
  const poolAfter = Math.max(totalStars, currentStars + selectedStars);
  const prob = (selectedStars / poolAfter * 100);
  const probDisplay = prob < 1 ? prob.toFixed(2) + '%' : prob.toFixed(1) + '%';
  document.getElementById('modalWinProb').textContent = probDisplay;

  // Update progress bar preview (show current + my stars)
  const previewPct = Math.min(100, ((currentStars + selectedStars) / totalStars) * 100);
  document.getElementById('modalProgressPreview').style.width = previewPct + '%';
}

