// ===== APPLY TRANSLATIONS =====
function applyLang(lang) {
  const dict = i18n[lang];
  if (!dict) return;
  currentLang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Update scenario prices (hardcoded in HTML, need JS update)
  const priceEls = document.querySelectorAll('.scenario-price-v2');
  if (priceEls.length >= 3) {
    priceEls[0].textContent = dict.scenario1_price || priceEls[0].textContent;
    priceEls[1].textContent = dict.scenario2_price || priceEls[1].textContent;
    priceEls[2].textContent = dict.scenario3_price || priceEls[2].textContent;
  }

  // Update notification times
  const timEls = document.querySelectorAll('.notif-time');
  if (timEls.length >= 3) {
    if (dict.notif_time1) timEls[0].textContent = dict.notif_time1;
    if (dict.notif_time2) timEls[1].textContent = dict.notif_time2;
    if (dict.notif_time3) timEls[2].textContent = dict.notif_time3;
  }

  // Update active lang option in dropdown
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  // Update trigger button display
  const activeOpt = document.querySelector(`.lang-option[data-lang="${lang}"]`);
  if (activeOpt) {
    const flag = activeOpt.getAttribute('data-flag');
    const flagEl = document.getElementById('langFlag');
    const codeEl = document.getElementById('langCode');
    if (flagEl) flagEl.textContent = flag;
    if (codeEl) codeEl.textContent = lang;
  }

  // Update html lang attribute
  const langMap = { VN: 'vi', CN: 'zh', ID: 'id' };
  document.documentElement.lang = langMap[lang] || 'vi';

  // Sync mobile lang buttons
  if (typeof syncMobileLangBtns === 'function') syncMobileLangBtns(lang);
}

