function filterProducts() {
  const query = (document.querySelector('.wish-search-input')?.value || '').toLowerCase();
  const sort = document.querySelector('.wish-sort-select')?.value || 'all';

  document.querySelectorAll('#productsGrid .product-card').forEach(card => {
    const cat = card.getAttribute('data-cat') || 'all';
    const name = (card.querySelector('.product-name')?.textContent || '').toLowerCase();
    const badge = card.querySelector('.product-badge');
    const badgeClass = badge ? badge.className : '';

    const catMatch = currentCat === 'all' || cat === currentCat;
    const queryMatch = !query || name.includes(query);
    let sortMatch = true;
    if (sort === 'hot') sortMatch = badgeClass.includes('hot');
    else if (sort === 'ending') sortMatch = badgeClass.includes('ending');
    else if (sort === 'new') sortMatch = badgeClass.includes('new');

    card.style.display = (catMatch && queryMatch && sortMatch) ? '' : 'none';
  });
}

